// *2. Assignment Operators (= , += , -= , *= , /= , %= , **=)
let a = 44;

a += 10; // a = a + 10 = 54
a -= 10; // a = a - 10 = 44
a *= 10; // a = a * 10 = 440
a /= 10; // a = a / 10 = 44
a %= 10; // a = a % 10 = 4
a **= 10; // a = a ** 10 = 44000000000


// *3. Comparison Operators (== , === , != , !== , > , < , >= , <=)
let num1 = 10;
let num2 = 10;

console.log(num1 == num2); // true // values
console.log(num1 === num2); // false // values and types
console.log(num1 != num2); // true
console.log(num1 !== num2); // true
console.log(num1 > num2); // false
console.log(num1 < num2); // true
console.log(num1 >= num2); // false (greater than or equal to)
console.log(num1 <= num2); // true (less than or equal to)




